let currentLang = 'ar';

function toggleLanguage() {
    currentLang = currentLang === 'ar' ? 'en' : 'ar';
    document.documentElement.dir = currentLang === 'ar' ? 'rtl' : 'ltr';
    document.documentElement.lang = currentLang;
    
    document.getElementById('lang-btn-text').textContent = currentLang === 'ar' ? 'English' : 'العربية';

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[currentLang][key]) {
            if (key === 'hero-title') {
                el.innerHTML = translations[currentLang][key];
            } else if (el.tagName === 'TITLE') {
                document.title = translations[currentLang][key];
            } else {
                el.textContent = translations[currentLang][key];
            }
        }
    });
}

function toggleTheme() {
    document.documentElement.classList.toggle('light');
    document.body.classList.toggle('light-theme');
}

function switchTab(tabId) {
    document.getElementById('tab-home').classList.add('hidden');
    document.getElementById('tab-share').classList.add('hidden');
    document.getElementById(`tab-${tabId}`).classList.remove('hidden');
}

function redirectToWhatsApp() {
    window.open('https://wa.me/201015571325', '_blank');
}

function toggleFaq(id) {
    const ans = document.getElementById(`faq-ans-${id}`);
    const icon = document.getElementById(`faq-icon-${id}`);
    
    if (ans.classList.contains('hidden')) {
        ans.classList.remove('hidden');
        icon.style.transform = 'rotate(180deg)';
    } else {
        ans.classList.add('hidden');
        icon.style.transform = 'rotate(0deg)';
    }
}

function copyLink() {
    navigator.clipboard.writeText(window.location.href).then(() => {
        const statusText = document.getElementById('copyStatus');
        const originalText = statusText.textContent;
        statusText.textContent = currentLang === 'ar' ? 'تم النسخ!' : 'Copied!';
        setTimeout(() => {
            statusText.textContent = originalText;
        }, 2000);
    });
}

function revealSections() {
    const reveals = document.querySelectorAll(".reveal");
    reveals.forEach((reveal) => {
        const windowHeight = window.innerHeight;
        const elementTop = reveal.getBoundingClientRect().top;
        if (elementTop < windowHeight - 50) {
            reveal.classList.add("active");
        }
    });
}

document.addEventListener("DOMContentLoaded", () => {
    revealSections();
    window.addEventListener("scroll", revealSections);
    
    const qrElement = document.getElementById("qrcode");
    if(qrElement && typeof QRCode !== 'undefined') {
        qrElement.innerHTML = "";
        new QRCode(qrElement, {
            text: window.location.href,
            width: 128,
            height: 128,
            colorDark : "#0f766e",
            colorLight : "#ffffff",
            correctLevel : QRCode.CorrectLevel.H
        });
    }
});